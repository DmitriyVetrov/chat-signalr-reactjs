export const lightenDarkenColor = (col: string, amt: number) => {
  var usePound = false;

  if (col[0] == '#') {
    col = col.slice(1);
    usePound = true;
  }

  var num = parseInt(col, 16);

  var r = (num >> 16) + amt;

  if (r > 255) r = 255;
  else if (r < 0) r = 0;

  var b = ((num >> 8) & 0x00ff) + amt;

  if (b > 255) b = 255;
  else if (b < 0) b = 0;

  var g = (num & 0x0000ff) + amt;

  if (g > 255) g = 255;
  else if (g < 0) g = 0;

  return (usePound ? '#' : '') + (g | (b << 8) | (r << 16)).toString(16);
};

export const stringToColor = (string: string) => {
  let hash = 0;
  let i;

  /* eslint-disable no-bitwise */
  for (i = 0; i < string.length; i += 1) {
    hash = string.charCodeAt(i) + ((hash << 5) - hash);
  }

  let color = '#';

  for (i = 0; i < 3; i += 1) {
    const value = (hash >> (i * 8)) & 0xff;
    color += `00${value.toString(16)}`.slice(-2);
  }
  /* eslint-enable no-bitwise */

  return color;
};

type avatarSize = 'small' | 'medium' | 'large' | undefined;

export const stringAvatar = (name: string, size?: avatarSize) => {
  const nameIsEmpty = name === '';
  const length = size === 'small' ? 24 : size === 'medium' ? 40 : size === 'large' ? 80 : 40;
  const fontSize = size === 'small' ? 10 : size === 'medium' ? 15 : size === 'large' ? 22 : 10;

  return {
    sx: {
      border: nameIsEmpty ? 0 : size === 'small' || size === 'medium' ? 1 : 2,
      borderColor: lightenDarkenColor(stringToColor(name), -20),
      bgcolor: nameIsEmpty ? 'transparent' : stringToColor(name),
      mr: size === 'small' || size === 'medium' ? 1 : 0,
      mt: size === 'small' || size === 'medium' ? 1 : 0,
      mb: size === 'small' || size === 'medium' ? 1 : 0,
      width: length,
      height: length,
      fontSize,
    },
    //children: `${name.split(" ")[0][0]}${name.split(" ")[1][0]}`,
    children: `${name[0]}${name[1]}`,
  };
};

export const browserName = (function () {
  var ua = navigator.userAgent;
  var tem;
  var M = ua.match(/(opera|chrome|safari|firefox|msie|trident(?=\/))\/?\s*(\d+)/i) || [];
  if (/trident/i.test(M[1])) {
    tem = /\brv[ :]+(\d+)/g.exec(ua) || [];
    return 'IE ' + (tem[1] || '');
  }
  if (M[1] === 'Chrome') {
    tem = ua.match(/\b(OPR|Edge)\/(\d+)/);
    if (tem != null) return tem.slice(1).join(' ').replace('OPR', 'Opera');
  }
  M = M[2] ? [M[1], M[2]] : [navigator.appName, navigator.appVersion, '-?'];
  if ((tem = ua.match(/version\/(\d+)/i)) != null) M.splice(1, 1, tem[1]);
  return M.join(' ');
})();
